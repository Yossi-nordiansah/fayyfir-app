  function cekkupon() {
    var kodevoucher = $('#kodevoucher').val();
    var tot = $('#totalpay3').val();
    $.ajax({
      url: '<?= base_url('cart/cekkupon') ?>',
      data: {
        kodevoucher: kodevoucher
      },
      type: 'post',
      dataType: 'json',
      success: function(result) {
        if (result == 1) {
          $.ajax({
            url: '<?= base_url('cart/detkupon') ?>',
            data: {
              kodevoucher: kodevoucher
            },
            type: 'post',
            dataType: 'json',
            success: function(res) {
              var disk = res.diskon;
              var sub = (disk / 100) * tot;
              var reverse = tot.toString().split('').reverse().join(''),
                ribuan = reverse.match(/\d{1,3}/g);
              ribuan = ribuan.join('.').split('').reverse().join('');
              $('#totalpaybef').html("<s>Rp " + ribuan + "</s> (" + disk + "%)");

              var diskon = tot - sub;
              var reverse = diskon.toString().split('').reverse().join(''),
                ribuan = reverse.match(/\d{1,3}/g);
              ribuan = ribuan.join('.').split('').reverse().join('');
              $('#totalpay2').html("Rp " + ribuan);

              $('#totalpaydisc').val(tot - sub);
              $('#voc').val(kodevoucher);
              var pot = Math.round(sub);
              $('#potongan').val(pot);

              $('#statVoc').removeClass('alert alert-warning');
              $('#statVoc').addClass('alert alert-success');
              $('#statVoc').html('Voucher found');
            }
          })

          //alert('kode berhasil');
        } else if (result == 0) {
          $('#statVoc').removeClass('alert alert-success');

          $('#statVoc').addClass('alert alert-warning');
          $('#statVoc').html('Voucher not fond');
        }
        // total_pay2();
        // total_pay();
      }
    });
  }

